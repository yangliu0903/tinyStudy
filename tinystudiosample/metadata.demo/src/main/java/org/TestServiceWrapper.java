package org;

import java.util.UUID;

import org.test.TestService;
import org.tinygroup.cepcore.CEPCore;
import org.tinygroup.context.Context;
import org.tinygroup.context.impl.ContextImpl;
import org.tinygroup.event.Event;
import org.tinygroup.event.ServiceRequest;

public class TestServiceWrapper implements TestService {

	CEPCore cepcore;

	public CEPCore getCore() {
		return cepcore;
	}

	public void setCore(CEPCore cepcore) {
		this.cepcore = cepcore;
	}

	private Event getEvent(String serviceId,Context context){
		Event event = new Event();
		event.setEventId(UUID.randomUUID().toString());
		ServiceRequest serviceRequest = new ServiceRequest();
		serviceRequest.setContext(context);
		serviceRequest.setServiceId(serviceId);
		event.setServiceRequest(serviceRequest);
		return event;
	}

	public void add(org.db.pojo.User user) {
		String serviceId = "add";

		try{
			Context context = new ContextImpl();
			context.put("user" ,user);

			callService(serviceId,context);
		}catch(Exception e){
			throw new RuntimeException(String.format("服务[%s]发生异常",serviceId),e);
		}
	}

	public void delete(int id) {
		String serviceId = "delete";

		try{
			Context context = new ContextImpl();
			context.put("id" ,id);

			callService(serviceId,context);
		}catch(Exception e){
			throw new RuntimeException(String.format("服务[%s]发生异常",serviceId),e);
		}
	}

	public void getAll() {
		String serviceId = "getAll";

		try{
			Context context = new ContextImpl();

			callService(serviceId,context);
		}catch(Exception e){
			throw new RuntimeException(String.format("服务[%s]发生异常",serviceId),e);
		}
	}

	public void get(int id) {
		String serviceId = "get";

		try{
			Context context = new ContextImpl();
			context.put("id" ,id);

			callService(serviceId,context);
		}catch(Exception e){
			throw new RuntimeException(String.format("服务[%s]发生异常",serviceId),e);
		}
	}

	private void callService(String serviceId,Context context){
		Event event = getEvent(serviceId,context);
		cepcore.process(event);
	}

}